<h2>5 ORDINARY COUNCIL MEETINGS</h2>
<ol type="a">
<li>in an election year, the annual meeting of the Council shall be held on or within 14 days following the day on which the councillors elected take office.</li>
<li>In a year which is not an election year, the annual meeting of the Council shall be held on such day in May as the Council decides.</li>
<li>If no other time is fixed, the annual meeting of the Council shall take place at 6pm.</li>
<li>In addition to the annual meeting of the Council, any number of other ordinary meetings may be held in each year on such dates and times as the Council decides.</li>
<li>The first business conducted at the annual meeting of the Council shall be the election of the Chairman and Vice-Chairman (if there is one) of the Council.</li>
<li>The Chairman of the Council, unless he/she has resigned or becomes disqualified, shall
continue in office and preside at the annual meeting until his/her successor is elected at the next annual meeting of the Council.</li>
<li>The Vice-Chairman of the Council if there is one, unless he/she resigns or becomes
disqualified, shall hold office until immediately after the election of the Chairman of the Council at the next annual meeting of the Council.</li>
<li>In an election year, if the current Chairman of the Council has not been re-elected as a member of the Council, he/she shall preside at the annual meeting until a successor
Chairman of the Council has been elected. The current Chairman of the Council shall not
have an original vote in respect of the election of the new Chairman of the Council but shall give a casting vote in the case of an equality of votes.</li>
<li>In an election year, if the current Chairman of the Council has been re-elected as a member of the Council, he/she shall preside at the annual meeting until a new Chairman of the Council has been elected. He may exercise an original vote in respect of the election of the new Chairman of the Council and shall give a casting vote in the case of an equality of votes.</li>
<li>Following the election of the Chairman of the Council and Vice-Chairman (if there is one) of the Council at the annual meeting, the business shall include:<br />
In an election year, delivery by the Chairman of the Council and councillors of their
acceptance of office forms unless the Council resolves for this to be done at a later
date. In a year which is not an election year, delivery by the Chairman of the Council of
his/her acceptance of office form unless the Council resolves for this to be done at a
later date;<br />
	<ol type="i">
	<li>Confirmation of the accuracy of the minutes of the last meeting of the Council;</li>
	<li>Receipt of the minutes of the last meeting of a committee;</li>
	<li>Consideration of the recommendations made by a committee;</li>
	<li>Review of delegation arrangements to committees, sub-committees, staff and other
local authorities;</li>
	<li>Review of the terms of reference for committees;</li>
	<li>Appointment of members to existing committees;</li>
	<li>Appointment of any new committees in accordance with standing order 4;</li>
	<li>Review and adoption of appropriate standing orders and financial regulations;</li>
	<li>Review of arrangements (including legal agreements) with other local authorities,
not-for-profit bodies and businesses;</li>
	<li>Review of representation on or work with external bodies and arrangements for
reporting back;</li>
	<li>Review of inventory of land and other assets including buildings and office equipment;</li>
	<li>Confirmation of arrangements for insurance cover in respect of all insurable risks;</li>
	<li>Review of the Council's and/or staff subscriptions to other bodies;</li>
	<li>Review of the Council's complaints procedure;</li>
	<li>Review of the Council's policies, procedures and practices in respect of its obligations under freedom of information and data protection legislation (see also standing orders 11, 20 and 21);</li>
	<li>Review of the Council's policy for dealing with the press/media;</li>
	<li>Review of the Council's employment policies and procedures;</li>
	<li>Review of the Council's expenditure incurred under s.137 of the Local Government Act 1972 or the power of well-being;</li>
	<li>Determining the time and place of ordinary meetings of the Council up to and including the next annual meeting of the Council.</li>
	</ol>
</li>
</ol>

<h2>6 EXTRAORDINARY MEETINGS OF THE COUNCIL, COMMITTEES AND SUB-COMMITTEES</h2>
<ol type="a">
<li>The Chairman of the Council may convene an extraordinary meeting of the Council at any
time.</li>
<li>If the Chairman of the Council does not call an extraordinary meeting of the Council within seven days of having been requested in writing to do so by two councillors, any two
councillors may convene an extraordinary meeting of the Council. The public notice giving
the time, place and agenda for such a meeting shall be signed by the two councillors.</li>
<li>The chairman of a committee or a sub-committee may convene an extraordinary meeting of
the committee or the sub-committee at any time.</li>
<li>f the chairman of a committee or a sub-committee does not call an extraordinary meeting within 7 days of having been requested to do so by 2 members of the committee or the sub-committee, any 2 members of the committee or the sub-committee may convene an
extraordinary meeting of the committee or the sub-committee.</li>
</ol>

<h2>7 PREVIOUS RESOLUTIONS</h2>
<ol type="a">
<li>A resolution shall not be reversed within six months except either by a special motion, which requires written notice by at least 5 councillors to be given to the Proper Officer in accordance with standing order 9, or by a motion moved in pursuance of the
recommendation of a committee or a sub-committee.</li>
<li>When a motion moved pursuant to standing order 7(a) has been disposed of, no similar
motion may be moved for a further six months.</li>
</ol>

<h2>8 VOTING ON APPOINTMENTS</h2>
<p>Where more than two persons have been nominated for a position to be filled by the Council
and none of those persons has received an absolute majority of votes in their favour, the
name of the person having the least number of votes shall be struck off the list and a fresh vote taken. This process shall continue until a majority of votes is given in favour of one person. A tie in votes may be settled by the casting vote exercisable by the chairman of the meeting.</p>

<h2>9 MOTIONS FOR A MEETING THAT REQUIRES WRITTEN NOTICE TO BE GIVEN TO THE PROPER OFFICER</h2>
<ol type="a">
<li>A motion shall relate to the responsibilities of the meeting for which it is tabled and in any event shall relate to the performance of the Council's statutory functions, powers and obligations or an issue which specifically affects the Council's area or its residents.</li>
<li>No motion may be moved at a meeting unless it is on the agenda and the mover has given
written notice of its wording to the Proper Officer at least 5 clear days before the meeting.<br />Clear days do not include the day of the notice or the day of the meeting.</li>
<li>The Proper Officer may, before including a motion on the agenda, received in accordance with standing order 9(b), correct obvious grammatical or typographical errors in the wording of the motion.</li>
<li>If the Proper Officer considers the wording of a motion received in accordance with standing order 9(b) is not clear in meaning, the motion shall be rejected until the mover of the motion re-submits it, so that it can be understood, in writing, to the Proper Officer at least 3 clear days before the meeting.</li>
<li>If the wording or subject of a proposed motion is considered improper, the Proper Officer shall consult with the chairman of the forthcoming meeting or, as the case may be, the councillors who have convened the meeting, to consider whether the motion shall be
included in the agenda or rejected.</li>
<li>The decision of the Proper Officer as to whether or not to include the motion on the agenda shall be final.</li>
<li>Motions received shall be recorded and numbered in the order that they are received.
h Motions rejected shall be recorded with an explanation by the Proper Officer of the reason for rejection.</li>
</ol>

<h2>10 MOTIONS AT A MEETING THAT DO NOT REQUIRE WRITTEN NOTICE</h2>
<ol type="a">
<li>The following motions may be moved at a meeting without written notice to the Proper
Officer:<br />
	<ol type="i">
	<li>to correct an inaccuracy in the draft minutes of a meeting;</li>
	<li>to move to a vote;</li>
	<li>to defer consideration of a motion;</li>
	<li>to refer a motion to a particular committee or sub-committee;</li>
	<li>to appoint a person to preside at a meeting;</li>
	<li>to change the order of business on the agenda;</li>
	<li>to proceed to the next business on the agenda;</li>
	<li>to require a written report;</li>
	<li>to appoint a committee or sub-committee and their members;</li>
	<li>to extend the time limits for speaking;</li>
	<li>to exclude the press and public from a meeting in respect of confidential or other
information which is prejudicial to the public interest;</li>
	<li>to not hear further from a councillor or a member of the public;</li>
	<li>to exclude a councillor or member of the public for disorderly conduct;</li>
	<li>to temporarily suspend the meeting;</li>
	<li>to suspend a particular standing order (unless it reflects mandatory statutory or legal requirements);</li>
	<li>to adjourn the meeting; or</li>
	<li>to close the meeting.</li>
	</ol>
</li>
</ol>
	
<h2>11 MANAGEMENT OF INFORMATION</h2>
<p>See also standing order 20</p>
<ol type="a">
<li>The Council shall have in place and keep under review, technical and organisational
measures to keep secure information (including personal data) which it holds in paper and
electronic form. Such arrangements shall include deciding who has access to personal data
and encryption of personal data.</li>
<li>The Council shall have in place, and keep under review, policies for the retention and safe destruction of all information (including personal data) which it holds in paper and electronic form. The Council's retention policy shall confirm the period for which information (including personal data) shall be retained or if this is not possible the criteria used to determine that period (e.g. the Limitation Act 1980).</li>
<li>The agenda, papers that support the agenda and the minutes of a meeting shall not disclose or otherwise undermine confidential information or personal data without legal justification.</li>
<li>Councillors, staff, the Council's contractors and agents shall not disclose confidential information or personal data without legal justification.</li>
</ol>

<h2>12 DRAFT MINUTES</h2>
<ol type="a">
<li>If the draft minutes of a preceding meeting have been served on councillors with the agenda to attend the meeting at which they are due to be approved for accuracy, they shall be taken as read.</li>
<li>There shall be no discussion about the draft minutes of a preceding meeting except in
relation to their accuracy. A motion to correct an inaccuracy in the draft minutes shall be moved in accordance with standing order 10(a)(i).</li>
<li>The accuracy of draft minutes, including any amendment(s) made to them, shall be
confirmed by resolution and shall be signed by the chairman of the meeting and stand as an
accurate record of the meeting to which the minutes relate.</li>
<li>If the chairman of the meeting does not consider the minutes to be an accurate record of the meeting to which they relate, he/she shall sign the minutes and include a paragraph in the following terms or to the same effect: "The chairman of this meeting does not believe that the minutes of the meeting of the Wyevalley Community Council held on [INSERT DATE] in respect of [INSERT SUMMARY] were a correct record but his/her view was not upheld by the meeting and the minutes are confirmed as an accurate record of the proceedings."</li>
<li>Subject to standing order 20 and following a resolution which confirms the accuracy of the minutes of a meeting, the draft minutes or recordings of the meeting for which approved minutes exist shall be destroyed</li>
</ol>

<h2>13 CODE OF CONDUCT AND DISPENSATIONS</h2>
<p>See also standing order 3(s)</p>
<ol type="a">
<li>Councillors and non-councillors with voting rights shall observe the code of conduct adopted by the Council.</li>
<li>All councillors and non-councillors with voting rights shall undertake training in the code of conduct within six months of the delivery of their acceptance of office form.</li>
<li>Dispensation requests shall be in writing and submitted to the standards committee of th e County Council as soon as possible before the meeting for which the dispensation is
required.</li>
</ol>

<h2>14 CODE OF CONDUCT COMPLAINTS</h2>
<ol type="a">
<li>Upon notification by the County Council that it is dealing with a complaint that a councillor or non-councillor with voting rights has breached the Council's code of conduct, the Proper Officer shall, subject to standing order 11, report this to the Council.</li>
<li>Where the notification in standing order 14(a) relates to a complaint made by the Proper Officer, the Proper Officer shall notify the Chairman of Council of this fact, and the Chairman shall nominate another staff member to assume the duties of the Proper Officer in relation to the complaint until it has been determined.</li>
<li>The Council shall deal with complaints of maladministration allegedly committed by the
Council or by any officer or member in such manner as adopted by the Council except for
those complaints, which should be properly directed to the Commissioner for consideration.</li>
<li>The Council may:<br />
	<ol type="i">
	<li>provide information or evidence where such disclosure is necessary to investigate the complaint or is a legal requirement;</li>
	<li>seek information relevant to the complaint from the person or body with statutory
responsibility for investigation of the matter;</li>
	<li>indemnify the councillor or non-councillor with voting rights in respect of his/her related legal costs and any such indemnity is subject to approval by a meeting of the Council.</li>
	</ol>
</li>
</ol>

