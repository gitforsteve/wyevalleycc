<h2>27 ORDER OF MEETINGS VIA THE INTERNET OR TELEPHONE</h2>
<ol type="a">
<li>BACKGROUND<br />
During times when Councillors are unable to meet face to face for any reason, such as a disease epidemic or local lockdown order, meetings may, with agreement of the Chair, be convened using an established internet application with voice and/or video capabilities or a group telephone call. The provisions of Standing Orders 1, 2 and 3 as to the conduct of the meetings generally shall apply except where amended below,<br /><br /></li>
<li>METHOD
	<ol type="i">
		<li>Any application or other system must be secure and encrypted end to end unless a group call is used.</li>
		<li>Councillors must ensure that their environment does not permit a third party to either view or overhear confidential items.</li>
	</ol>
</li>
<li>NOTICE AND PARTICIPANTS
	<ol type="i">
		<li>Notice of the meeting must be given in the usual way with the accepted notice period.</li>
		<li>Notice must be given that members of the public can submit questions or other items in writing but will not be able to take part in any part of the meeting. This is because of the difficulty in ensuring that members of the public have left the meeting prior to the discussion of confidential items.</li>
		<li>The meeting must be quorate and non-attendance of such meetings will be treated the same as non attendance at a normal meeting.</li>
	</ol>
</li>
<li>PROCEDURE
	<ol type="i">
		<li>The meeting will be minuted in the normal way and be scheduled in line with current requirements and legislation.</li>
		<li>The agenda will be followed under the direction of the Chair</li>
		<li>The Chair will refer to each Councillor in turn for his or her comments</li> 
		<li>If a discussion is required each Councillor will express his or her intention to add to the discussion by saying his or her name and the Chair will confirm when they are allowed to speak and, if necessary, to make a proposal. If a proposal is made the Chair will ask for a seconder and, if the proposal is seconded, ask each Councillor to express his or her support of the proposal or otherwise, the proposal  will then be handled in the usual way.</li>
		<li>After each Councillor has spoken the Chair will ensure that the Clerk has been able to accurately record what was said.</li>
		<li>Any other business should be kept to a minimum by notifying the Clerk in time for inclusion in the agenda but otherwise treated as the other agenda items in turn.</li>
	</ol>
</ol>
