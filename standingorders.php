</ol>

<h2>16 RESPONSIBLE FINANCIAL OFFICER</h2>
<p>The Responsible Financial Officer shall be either (i) the clerk or (ii) other staff member(s) nominated by the Council to undertake the work of the Responsible Financial Officer when the Responsible Financial Officer is absent.</p>

<h2>17 ACCOUNTS AND ACCOUNTING STATEMENTS</h2>
<ol type="a">
<li>"Proper practices" in standing orders refer to the most recent version of "Governance and Accountability for Local Councils in Wales - A Practitioners' Guide".</li>
<li>All payments by the Council shall be authorised, approved and paid in accordance with the law, proper practices and the Council's financial regulations.</li>
<li>As soon as possible after the financial year end at 31 March, the Responsible Financial Officer shall provide:<br />
each councillor with a statement summarising the Council's receipts and payments (or
income and expenditure) for the year to date for information; and to the Council the accounting statements for the year in the form of Section 1 of the annual governance and accountability return, as required by proper practices, for consideration and approval.</li>
<li>The year-end accounting statements shall be prepared in accordance with proper practices and apply the form of accounts determined by the Council (receipts and payments or income and expenditure) for the year to 31 March. A completed draft annual governance and accountability return shall be presented to all councillors at least 14 days prior to anticipated approval by the Council. The annual governance and accountability return of the Council, which is subject to external audit, including the annual governance statement, shall be presented to the Council for consideration and formal approval before 30 June.</li>
</ol>

<h2>18 FINANCIAL CONTROLS AND PROCUREMENT</h2>
<ol type="a">
<li>The Council shall consider and approve financial regulations drawn up by the Responsible Financial Officer, which shall include detailed arrangements in respect of the following:<br />
	<ol type="i">
	<li>the keeping of accounting records and systems of internal controls;</li>
	<li>the assessment and management of financial risks faced by the Council;</li>
	<li>the work of the independent internal auditor in accordance with proper practices and the receipt of regular reports from the internal auditor, which shall be required at least annually;</li>
	<li>the inspection and copying by councillors and local electors of the Council's accounts and/or orders of payments; and</li>
	<li>subject to standing orders 18(e) and (f) whether contracts with an estimated value
below &pound;25,000 or due to special circumstances are exempt from a tendering process or
procurement exercise.</li>
	</ol>
</li>
<li>Financial regulations shall be reviewed regularly and at least annually for fitness of purpose.</li>
<li>Subject to additional requirements in the financial regulations of the Council, the tender process for contracts for the supply of goods, materials, services or the execution of works shall include, as a minimum, the following steps:<br />
	<ol type="i">
	<li>specification for the goods, materials, services or the execution of works shall be drawn up;</li>
	<li>an invitation to tender shall be drawn up to confirm (i) the Council's specification (ii) the time, date and address for the submission of tenders (iii) the date of the Council's written response to the tender and (iv) the prohibition on prospective contractors contacting councillors or staff to encourage or support their tender outside the prescribed process;</li>
	<li>the invitation to tender shall be advertised in a local newspaper and in any other
manner that is appropriate;</li>
	<li>tenders are to be submitted in writing in a sealed marked envelope addressed to the Proper Officer;</li>
	<li>tenders shall be opened by the Proper Officer in the presence of at least one councillor after the deadline for submission of tenders has passed;</li>
	<li>tenders are to be reported to and considered by the appropriate meeting of the
Council or a committee or sub-committee with delegated responsibility.</li>
	</ol>
</li>
<li>Neither the Council, nor a committee or a sub-committee with delegated responsibility for considering tenders, is bound to accept the lowest value tender.</li>
<li>A public contract regulated by the Public Contracts Regulations 2015 with an estimated value in excess of &pound;181,302 for a public service or supply contract or in excess of &pound;4,551,413 for a public works contract (or other thresholds determined by the European Commission every two years and published in the Official Journal of the European Union (OJEU)) shall comply with the relevant procurement procedures and other requirements in the Public Contracts Regulations 2015 which include advertising the contract opportunity on the Contracts Finder website and in OJEU.</li>
<li>A public contract in connection with the supply of gas, heat, electricity, drinking water, transport services, or postal services to the public; or the provision of a port or airport; or the exploration for or extraction of gas, oil or solid fuel with an estimated value in excess of &pound;363,424 for a supply, services or design contract; or in excess of &pound;4,551,413 for a works contract; or &pound;820,370 for a social and other specific services contract (or other thresholds determined by the European Commission every two years and published in OJEU) shall comply with the relevant procurement procedures and other requirements in the Utilities Contracts Regulations 2016.</li>
</ol>

<h2>19 HANDLING STAFF MATTERS</h2>
<ol type="a">
<li>A matter personal to a member of staff that is being considered by a meeting of Council is subject to standing order 11.</li>
<li>Subject to the Council's policy regarding absences from work, the Council's most senior member of staff shall notify the Chairman of the Council or, if he/she is not available, the Vice-Chairman (if there is one) of absence occasioned by illness or other reason and that person shall report such absence to the Council at its next meeting.</li>
<li>The Chairman of the Council or in his/her absence, the Vice-Chairman shall upon a
resolution conduct a review of the performance and annual appraisal of the work of the
member of staff. The reviews and appraisal shall be reported in writing and are subject to
approval by resolution by the Council.</li>
<li>Subject to the Council's policy regarding the handling of grievance matters, the Council's most senior member of staff (or other members of staff) shall contact the Chairman of the Council or in his/her absence, the Vice-Chairman in respect of an informal or formal grievance matter, and this matter shall be reported back and progressed by resolution of the Council.</li>
<li>Subject to the Council's policy regarding the handling of grievance matters, if an informal or formal grievance matter raised by the member of staff relates to the Chairman or Vice-Chairman of the Council, this shall be communicated to another member of the Council, which shall be reported back and progressed by resolution of the Council.</li>
<li>Any persons responsible for all or part of the management of staff shall treat as confidential the written records of all meetings relating to their performance, capabilities, grievance or disciplinary matters.</li>
<li>In accordance with standing order 11(a), persons with line management responsibilities shall have access to staff records referred to in standing order 19(f).</li>
</ol>

