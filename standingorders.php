at least three clear days before a meeting of the council, a committee or a
sub-committee:<br />serve on councillors by delivery or post at their residences a signed summons confirming the time, place and the agenda; and provide, in a conspicuous place, public notice of the time, place and agenda (provided that the public notice with agenda of an extraordinary meeting of the Council convened by councillors is signed by them) and publish electronically notice of the time and place and, as far as reasonably practicable, any documents relating to the business to be transacted at the meeting unless they relate to business which is likely to be considered in private or if their disclosure would be contrary to any enactment. See standing order 3(b) for the meaning of clear days for a meeting of a full council and standing order 3(c) for the meaning of clear days for a meeting of a committee;<br />
