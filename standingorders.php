<h1>STANDING ORDERS</h1>
<p>ADOPTED BY COUNCIL 28 SEPTEMBER 2020</p>
<h2>1 RULES OF DEBATE AT MEETINGS</h2>
<ol type="a">
<li>Motions on the agenda shall be considered in the order that they appear unless the order is changed at the discretion of the chairman of the meeting.</li>
<li>A motion (including an amendment) shall not be progressed unless it has been moved and seconded.</li>
<li>A motion on the agenda that is not moved by its proposer may be treated by the chairman of the meeting as withdrawn.</li>
<li>If a motion (including an amendment) has been seconded, it may be withdrawn by the
proposer only with the consent of the seconder and the meeting.</li>
<li>An amendment is a proposal to remove or add words to a motion. It shall not negate the motion.</li>
<li>If an amendment to the original motion is carried, the original motion (as amended)
becomes the substantive motion upon which further amendment(s) may be moved.</li>
<li>An amendment shall not be considered unless early verbal notice of it is given at the meeting and, if requested by the chairman of the meeting, is expressed in writing to the chairman.</li>
<li>A councillor may move an amendment to his/her own motion if agreed by the meeting. If a motion has already been seconded, the amendment shall be with the consent of the
seconder and the meeting.</li>
<li>If there is more than one amendment to an original or substantive motion, the amendments shall be moved in the order directed by the chairman of the meeting.</li>
<li>Subject to standing order 1(k), only one amendment shall be moved and debated at a time, the order of which shall be directed by the chairman of the meeting.</li>
<li>One or more amendments may be discussed together if the chairman of the meeting
considers this expedient but each amendment shall be voted upon separately</li>
<li>A councillor may not move more than one amendment to an original or substantive motion.</li>
<li>The mover of an amendment has no right of reply at the end of debate on it.</li>
<li>Where a series of amendments to an original motion are carried, the mover of the original motion shall have a right of reply either at the end of debate on the first amendment or at the very end of debate on the final substantive motion immediately before it is put to the vote.</li>
<li>Unless permitted by the chairman of the meeting, a councillor may speak once in the debate on a motion except:<br />
to speak on an amendment moved by another councillor;
	<ol type="i">
	<li>to move or speak on another amendment if the motion has been amended since
he/she last spoke;</li>
	<li>to make a point of order;</li>
	<li>to give a personal explanation; or</li>
	<li>to exercise a right of reply.</li>
	</ol>
</li>
<li>During the debate on a motion, a councillor may interrupt only on a point of order or a personal explanation and the councillor who was interrupted shall stop speaking. A
councillor raising a point of order shall identify the standing order which he/she considers has been breached or specify the other irregularity in the proceedings of the meeting he/she is concerned by.</li>
<li>A point of order shall be decided by the chairman of the meeting and his/her decision shall be final.</li>
<li>When a motion is under debate, no other motion shall be moved except: to amend the motion;<br />
	<ol type="i">
	<li>to proceed to the next business;</li>
	<li>to adjourn the debate;</li>
	<li>to put the motion to a vote;</li>
	<li>to refer a motion to a committee or sub-committee for consideration;</li>
	<li>to exclude the public and press;</li>
	<li>to adjourn the meeting; or</li>
	<li>to suspend particular standing order(s) excepting those which reflect mandatory
statutory or legal requirements</li>
	</ol>
</li>
<li>Before an original or substantive motion is put to the vote, the chairman of the meeting shall be satisfied that the motion has been sufficiently debated and that the mover of the motion under debate has exercised or waived his/her right of reply.</li>
<li>Excluding motions moved under standing order 1(r), the contributions or speeches by a
councillor shall relate only to the motion under discussion and shall not exceed 5 minutes
without the consent of the chairman of the meeting.</li>
</ol>


<h2>2 DISORDERLY CONDUCT AT MEETINGS</h2>
<ol type="a">
<li>No person shall obstruct the transaction of business at a meeting or behave offensively or improperly. If this standing order is ignored, the chairman of the meeting shall request such person(s) to moderate or improve their conduct.</li>
<li>If person(s) disregard the request of the chairman of the meeting to moderate or improve their conduct, any councillor or the chairman of the meeting may move that the person be no longer heard or be excluded from the meeting. The motion, if seconded, shall be put to the vote without discussion.</li>
<li>If a resolution made under standing order 2(b) is ignored, the chairman of the meeting may take further reasonable steps to restore order or to progress the meeting. This may include temporarily suspending or closing the meeting.</li>
</ol>

<h2>3 MEETINGS GENERALLY</h2>
<ol type="a">
<li>Meetings shall not take place in premises which at the time of the meeting are used for the supply of alcohol, unless no other premises are available free of charge or at a reasonable cost.</li>
<li>The minimum three clear days for notice of a meeting does not include the day on which notice was issued, the day of the meeting, a Sunday, a day of the Christmas break, a day of the Easter break or of a bank holiday or a day appointed for public thanksgiving or
mourning.</li>
<li>The minimum three clear days public notice for a meeting does not include the day on which the notice was issued or the day of the meeting unless the meeting is convened at shorter notice.</li>
<li>Meetings shall be open to the public unless their presence is prejudicial to the public interest by reason of the confidential nature of the business to be transacted or for other special reasons. The public's exclusion from part or all of a meeting shall be by a resolution which shall give reasons for the public's exclusion.</li>
<li>Members of the public may make representations, answer questions and give evidence at a meeting which they are entitled to attend in respect of the business on the agenda.</li>
<li>The period of time designated for public participation at a meeting in accordance with
standing order 3(e) shall not exceed 15 minutes unless directed by the chairman of the
meeting.</li>
<li>Subject to standing order 3(f), a member of the public shall not speak for more than 5
minutes.</li>
<li>In accordance with standing order 3(e), a question shall not require a response at the
meeting nor start a debate on the question. The chairman of the meeting may direct that a
written or oral response be given.<br />
	<ol type="i">
	<li>A person shall raise his/her hand when requesting to speak (except when a person has a disability or is likely to suffer discomfort).</li>
	<li>A person who speaks at a meeting shall direct his/her comments to the chairman of the meeting.</li>
	</ol>
</li>
<li>Only one person is permitted to speak at a time. If more than one person wants to speak, the chairman of the meeting shall direct the order of speaking.</li>
<li>Photographing, recording, broadcasting or transmitting the proceedings of a meeting by any means is not permitted without the Council's prior written consent.</li>
<li>The press shall be provided with reasonable facilities for the taking of their report of all or part of a meeting at which they are entitled to be present.</li>
<li>Subject to standing orders which indicate otherwise, anything authorised or required to be done by, to or before the Chairman of the Council may in his/her absence be done by, to or before the Vice-Chairman of the Council (if there is one).</li>
<li>The Chairman of the Council, if present, shall preside at a meeting. If the Chairman is absent from a meeting, the Vice-Chairman of the Council (if there is one), if present, shall preside. If both the Chairman and the Vice-Chairman are absent from a meeting, a councillor as chosen by the councillors present at the meeting shall preside at the meeting.</li>
<li>Subject to a meeting being quorate, all questions at a meeting shall be decided by a majority of the councillors and non-councillors with voting rights present and voting.</li>
<li>The chairman of a meeting may give an original vote on any matter put to the vote, and in the case of an equality of votes may exercise his/her casting vote whether or not he/she gave an original vote. (See standing orders 5(h) and (i) for the different rules that apply in the election of the Chairman of the Council at the annual meeting of the Council.)</li>
<li>Unless standing orders provide otherwise, voting on a question shall be by a show of hands.<br />
At the request of a councillor, the voting on any question shall be recorded so as to show
whether each councillor present and voting gave his/her vote for or against that question.
Such a request shall be made before moving on to the next item of business on the agenda.</li>
<li>The minutes of a meeting shall include an accurate record of the following:
the time and place of the meeting;<br />
	<ol type="i">
	<li>the names of councillors who are present and the names of councillors who are absent;</li>
	<li>interests that have been declared by councillors and non-councillors with voting rights;</li>
	<li>the grant of dispensations (if any) to councillors and non-councillors with voting rights;</li>
	<li>whether a councillor or non-councillor with voting rights left the meeting when matters that they held interests in were being considered;</li>
	<li>if there was a public participation session; and</li>
	<li>the resolutions made.</li>
	</ol>
</li>
<li>A councillor or a non-councillor with voting rights who has a personal or prejudicial interest in a matter being considered at a meeting which limits or restricts his/her right to participate in a discussion or vote on that matter is subject to obligations in the code of conduct adopted by the Council.</li>
<li>No business may be transacted at a meeting unless at least one-third of the whole number of members of the Council are present and in no case shall the quorum of a meeting be less than 3. (See standing order 4(d)(viii) for the quorum of a committee or sub-committee meeting.)</li>
<li>If a meeting is or becomes inquorate no business shall be transacted and the meeting shall be closed. The business on the agenda for the meeting shall be adjourned to another
meeting.</li>
<li>Evening meetings shall commence at 19:00 and end by 21:30. Any unfinished business is to be carried forward to the next agreed meeting.</li>
</ol>

<h2>4 COMMITTEES AND SUB-COMMITTEES</h2>
<ol type="a">
<li>Unless the Council determines otherwise, a committee may appoint a sub-committee whose
terms of reference and members shall be determined by the committee.</li>
<li>The members of a committee may include non-councillors unless it is a committee which
regulates and controls the finances of the Council.</li>
<li>Unless the Council determines otherwise, all the members of an advisory committee and a sub-committee of the advisory committee may be non-councillors.</li>
<li>The Council may appoint standing committees or other committees as may be necessary,
and:<br />
shall determine their terms of reference;
	<ol type="i">
	<li>shall determine the number and time of the ordinary meetings of a standing
committee up until the date of the next annual meeting of the Council;</li>
	<li>shall permit a committee, other than in respect of the ordinary meetings of a
committee, to determine the number and time of its meetings;</li>
	<li>shall, subject to standing orders 4(b) and (c), appoint and determine the terms of office of members of such a committee;</li>
	<li>may, subject to standing orders 4(b) and (c), appoint and determine the terms of office of the substitute members to a committee whose role is to replace the ordinary
members at a meeting of a committee if the ordinary members of the committee
confirm to the Proper Officer 3 days before the meeting that they are unable to attend;</li>
	<li>shall, after it has appointed the members of a standing committee, appoint the
chairman of the standing committee;</li>
	<li>shall permit a committee other than a standing committee, to appoint its own
chairman at the first meeting of the committee;</li>
	<li>shall determine the place, notice requirements and quorum for a meeting of a
committee and a sub-committee which, in both cases, shall be no less than 2</li>
	<li>shall determine if the public may participate at a meeting of a committee;</li>
	<li>shall determine if the public and press are permitted to attend the meetings of a
sub-committee and also the advance public notice requirements, if any, required for
the meetings of a sub-committee;</li>
	<li>shall determine if the public may participate at a meeting of a sub-committee that they are permitted to attend; and</li>
	<li>may dissolve a committee or a sub-committee</li>
	</ol>
</li>
</ol>
