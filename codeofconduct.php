<h1>Code of Conduct</h1>
<p>Adopted at a meeting on 27th March 2017<br />as recommended by the Welsh Assembly</p>
<h2>PART 1 INTERPRETATION</h2>
  <p style="margin-left:20px;text-indent:-20px;">1. (1) In this code-<br />
  “co-opted member” (“aelod cyfetholedig”), in relation to a relevant authority, means
  a person who is not a member of the authority but who-</p>
  <ol type="a">
    <li>Is a member of any committee or sub-committee of the authority, or</li>
    <li>Is a member of, and represents the authority on, any joint committee or joint sub-committee of the authority, and who is entitled to vote on any question which falls to be decided at any meeting of that committee or sub-committee;</li>
  </ol>
  <p style="margin-left:20px;text-indent:-20px;">“meeting” (“cyfarfod”) means any meeting-</p>
  <ol type="a">
    <li>of the relevant authority</li>
    <li>of any executive or board of the relevant authority,</li>
    <li>of any committee, sub-committee, joint committee or joint sub-committee of the relevant authority or of any such committee, sub-committee, joint committee or joint sub-committee of any executive or board of the authority, or</li>
    <li>where members or officers of the relevant authority are present other than a meeting of a political group constituted in accordance with Regulation 8 of the Local Government (Committees and Political Groups) Regulations 1990(1), and includes circumstances in which a member of an executive or board or an officer acting alone exercises a function of an authority;</li>
  </ol>
  <p>“member” (“aelod”) includes, unless the context requires otherwise, a co-opted member,
    “relevant authority” (“awdurdodperthnasol”) means</p>
  <ol type="a">
    <li>a county council,</li>
    <li>a county borough council,</li>
    <li>a community council,</li>
    <li>a fire and rescue authority constituted by a scheme under Section 2 of the Fire and Rescue Services Act 2004 (2) or a scheme to which Section 4 of that Act applies,</li>
    <li>a National Park authority established under Section 63 of the Environment Act 1995 (3);</li>
  </ol>
  <p>“you” (“chi”) means you as a member or co-opted member of a relevantAuthority; and<br />
“your authority” (“eich awdurdod”) means the relevant authority of which you are a member or co-opted member.</p>

<p style="margin-left:20px;text-indent:-20px;">(2) In relation to a community council, references to an authority’s monitoring officer and an authority’s standards committee are to be read, respectively, as references to the monitoring officer and the standards committee of the county or county borough council which has functions in relation to the community council for which it is responsible under Section 56 (2) of the Local Government Act 2000.</p>

<h2>PART 2 GENERAL PROVISIONS</h2>
<p style="margin-left:20px;text-indent:-20px;">2. (1) Save where paragraph 3(a) applies, you must observe this code of conduct-</p>
<ol type="a">
  <li>whenever you conduct the business, or are present at a meeting of your authority;</li>
  <li>whenever you act, claim to act, or give the impression you are acting in the role of member to which you were elected or appointed;</li>
  <li>whenever you act, claim to act, or give the impression you are acting as a representative of your authority; or<br />(d) at all times and in any capacity, in respect of conduct identified in paragraphs 6(1)(a) and 7.</li>
</ol>
<p style="margin-left:20px;text-indent:-20px;">(2) You should read this code together with the general principles prescribed under Section 49(2) of the Local Government Act 2000 in relation to Wales.</p>

<p style="margin-left:20px;text-indent:-20px;">3. Where you are elected, appointed or nominated by your authority to serve-</p>
<ol type="a">
  <li>on another relevant authority, or any other body, which includes a police authority or Local Health Board you must, when acting for that other authority or body, comply with the code of conduct of that other authority or body; or</li>
  <li>on any other body which does not have a code relating to the conduct of its members, you must, when acting for that other body, comply with this code of conduct, except and insofar as it conflicts with any other lawful obligations to which that other body may be subject.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">4. You must-</p>
<ol type="a">
  <li>carry out your duties and responsibilities with due regard to the principle that there should be equality of opportunity for all people, regardless of their gender, race, disability, sexual orientation, age or religion;</li>
  <li>show respect and consideration for others;</li>
  <li>not use bullying behaviour or harass any person; and</li>
  <li>not do anything which compromises, or which is likely to compromise, the impartiality of those who work for, or on behalf of, your authority.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">5. You must not</p>
<ol type="a">
  <li>disclose confidential information or information which should reasonably be regarded as being of a confidential nature, without the express consent of a person authorised to give such consent, or unless required by law to do so;</li>
  <li>prevent any person from gaining access to information to which that person is entitled by law.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">6. (1) You must-</p>
<ol type="a">
  <li>not conduct yourself in a manner which could reasonably be regarded as bringing your office or authority into disrepute</li>
  <li>report, whether through your authority’s confidential reporting procedure or direct to the property authority, any conduct by another member or anyone who works for, or on behalf of, your authority which you reasonably believe involves or is likely to involve criminal behaviour (which for the purposes of this paragraph does not include offences or behaviour capable of punishment byway of a fixed penalty);</li>
  <li>report to the Public Services Ombudsman for Wales and to your authority’s monitoring officer any conduct by another member which you reasonably believe breaches this code of conduct;</li>
  <li>not make vexatious, malicious or frivolous complaints against other members or anyone who works for, or on behalf of, your authority.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;"> (2) You must comply with any request of your authority’s monitoring officer, or the Public Services Ombudsman for Wales, in connection with an investigation conducted in accordance with their respective statutory powers.</p>

<p style="margin-left:20px;text-indent:-20px;">7. You must not-</p>
<ol type="a">
  <li>in your official capacity or otherwise, use or attempt to use your position improperly to confer on or secure for yourself, or any other person, an advantage or create or avoid for yourself, or any other person, a disadvantage;</li>
  <li>use, or authorise others to use, the resources of your authority -<br />
    <ol type="i">
      <li>imprudently;</li>
      <li>in breach of your authority’s requirements;</li>
      <li>unlawfully;</li>
      <li>other than in a manner which is calculated to facilitate, or to be conducive to, the discharge of the functions of the authority or of the office to which you have been elected or appointed;</li>
      <li>improperly for political purposes; or</li>
      <li>improperly for private purposes.</li>
    </ol>
  </li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">8. You must -</p>
<ol type="a">
  <li>when participating in meetings or reaching decisions regarding the business of your authority, do so on the basis of the merits of the circumstances involved and in the public interest having regard to any relevant advice provided by your authority’s officers, in particular
    <ol type="i">
      <li>the authority’s head of paid service;</li>
      <li>the authority’s chief financial officer;</li>
      <li>the authority’s monitoring officer;</li>
      <li>the authority’s chief legal officer (who should be consulted when there is any doubt as to the authority’s power to act, as to whether the action proposed lies within the policy framework agreed by the authority or where the legal consequences of action or failure to act by the authority might have important repercussions);</li>
    </ol>
  </li>
  <li>give reasons for all decisions in accordance with any statutory requirements and any reasonable additional requirements imposed by your authority.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">9. You must -</p>
<ol type="a">
  <li>observe the law and your authority’s rules governing the claiming of expenses and allowances in connection with your duties as a member;</li>
  <li>avoid accepting from anyone gifts, hospitality (other than official hospitality, such as a civic reception or a working lunch duly authorised by your authority), material benefits or services for yourself or any person which might place you, or reasonably appear to place you, under an improper obligation.</li>
</ol>

<h2>PART 3 INTERESTS</h2>
<h3>Personal Interests</h3>
<p style="margin-left:20px;text-indent:-20px;">10. (1) You must in all matters consider whether you have a personal interest, and whether this code of conduct requires you to disclose that interest.</p>
<p style="margin-left:20px;text-indent:-20px;">(2) You must regard yourself as having a personal interest in any business of your authority if -</p>
<ol type="a">
 <li>it relates to, or is likely to affect -
   <ol type="i">
     <li>any employment or business carried on by you;</li>
     <li>any person who employs or has appointed you, any firm in which you are a partner or any company for which you are a remunerated director;</li>
     <li>any person, other than your authority, who has made a payment to you in respect of your election or any expenses incurred by you in carrying out your duties as a member;</li>
     <li>any corporate body which has a place of business or land in your authority’s area, and in which you have a beneficial interest in a class of securities of that body that exceeds the nominal value of &pound;25,000 or one hundredth of the total issued share capital of that body;</li>
     <li>any contract for goods, services or works made between your authority and you or a firm in which you are a partner, a company of which you are a remunerated director, or a body of the description specified in sub-paragraph (iv)above:</li>
    <li>any land in which you have a beneficial interest and which is in the area of your authority;</li>
    <li>any land where the landlord is your authority and the tenant is a firm in which you are a partner, a company of which you are a remunerated director, or a body of the description specified in sub-paragraph (iv) above;</li>
    <li>any body to which you have been elected, appointed or nominated by your authority;</li>
<li>any -
  <ol type="A">
    <li>public authority or body exercising functions of a public nature;</li>
    <li>company, industrial and provident society, charity, or body directed to charitable purposes;</li>
    <li>body whose principal purposes include the influence of public opinion or policy;</li>
    <li>trade union or professional association; or>/li>
    <li>private club, society or association operating within your authority’s area, in which you have membership or hold a position of general control or management;</li>
  </ol>
</li>
   <li>any land in your authority’s area in which you have a licence (alone or jointly with others) to occupy for 28 days or longer</li>
  </ol>
 <li>a member of the public might reasonably perceive a conflict between your role in taking a decision, upon that business, on behalf of your authority as a whole and your role in representing the interests of constituents in your ward or electoral decision; or</li>
<li>a decision upon it might reasonably be regarded as affecting -
  <ol type="i">
    <li>your well-being or financial position, or that of a person with whom you live, or any person with whom you have a close personal association;</li>
    <li>your well-being or financial position, or that of a person with whom you live, or any person with whom you have a close personal association;</li>
    <li>any employment or business carried out on by persons as described in 10(2)(c)(i);</li>
    <li>any person who employs or has appointed such persons described in 10(2)(c)(i), any firm in which they are a partner, or any company of which they are a partner, or any company of which they are directors;</li>
    <li>any corporate body in which persons as described in 10(2)(c)(i) have a beneficial interest in a class of securities exceeding the nominal value of &pound;5,000; or</li>
<li>any body listed in paragraphs 10(2)(a)(ix)(aa) to (ee) in which persons described hold a position of general control or management, to a greater extent than the majority of-
  <ol type="A">
    <li>in case of an authority with electoral divisions or wards, other council tax payers, rate payers or inhabitants of the electoral division or ward, as the case may be affected by the decision; or</li>
    <li>in all other cases, other council tax payers, ratepayers or inhabitants of the authority’s area.</li>
  </ol>
</li>
  </ol>

<h3>Disclosure of Personal Interests</h3>
<p style="margin-left:20px;text-indent:-20px;">12 (1) Subject to sub-paragraph (2) below, where you have a personal interest in any business of your authority you also have a prejudicial interest in that business if the interest is one which a member of the public with knowledge of the relevant facts would reasonably regard as so significant that it is likely to prejudice your judgement of the public interest.</p>
<p style="margin-left:20px;text-indent:-20px;">(2) Subject to sub-paragraph (3), you will not be regarded as having a prejudicial interest in any business where that business -</p>
<ol type="a">
<li>relates to -
  <ol type="i">
    <li>another relevant authority of which you are also a member;</li>
    <li>another public authority or body exercising functions of a public nature< in which you hold a position of general control or management;</li>
    <li>a body to which you have been elected, appointed or nominated by your authority;</li>
    <li>your role as a school governor (where not appointed or nominated by your authority) unless it relates particularly to the school of which you are a governor;</li>
    <li>your role as a member of a Local Health Board where you have not been appointed or nominated by your authority;</li>
  </ol></li>
<li>relates to -
  <ol type="i">
    <li>the housing functions of your authority where you hold a tenancy or lease with your authority, provided that you do not have arrears of rent with your authority of more than two months, and provided that those functions do not relate particularly to your tenancy or lease;</li>
    <li>the functions of your authority in respect of school meals, transport and travelling expenses, where you are a guardian, parent, grandparent or have parental responsibility (as defined in Section 3 of the Children Act 1989) of a child in full time education, unless it relates particularly to the school which that child attends;</li>
    <li>the functions of your authority in respect of statutory sick pay under Party XI of the Social Security Contributions &amp; Benefits Act 1992, where you are in receipt of, or are entitled to the receipt of such pay from your authority;</li>
    <li>the functions of your authority in respect of an allowance or payment made under Sections 22(5), 24(4) and 173 to 176 of the Local Government Act 1972, an allowance or pension under Section 18 of the Local Government and Housing Act 1989 or an allowance or payment under Section 100 of the Local Government Act 2000;</li>
  </ol></li>
  <li>your role as a community councillor in relation to a grant, loan or form of financial assistance made by your community council to community or voluntary organisations up to a maximum of &pound;500.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">(3) The exemptions in sub-paragraph (2)(a) do not apply where the business relates to the determination of any approval, consent, licence, permission or registration.</p>

<p style="margin-left:20px;text-indent:-20px;">13. You also have a prejudicial interest in any business before an overview and scrutiny committee of your authority (or of a sub-committee of such a committee) where-</p>
<ol type="a">
  <li>that business relates to a decision made (whether implemented or not) or action taken by your authority’s executive, board or another of your authority’s committees, sub-committees, joint committees or joint sub-committees; and</li>
  <li> at the time the decision was made or action was taken, you were a member of the executive, board, committee, sub-committee, joint-committee or joint sub-committee mentioned in sub-paragraph (a) and you were present when that decision was made or action was taken.</li>
</ol>

<h3>Participation in Relation to Disclosed Interests</h3>
<p style="margin-left:20px;text-indent:-20px;">14. (1) Subject to sub-paragraphs (2), (3) and (4), where you have a prejudicial interest in any business of your authority you must, unless you have obtained a dispensation from your authority’s standards committee-</p>
<ol type="a">
  <li>withdraw from the room, chamber or place where a meeting considering the business is being held-
    <ol type="i">
      <li>where sub-paragraph (2) applies, immediately after the period for making representations, answering questions or giving evidence relating to the business has ended and in any event before further consideration of the business begins, whether or not the public are allowed to remain in attendance for such consideration; or</li>
      <li>in any other case, whenever it becomes apparent that that business is being considered at that meeting;</li>
    </ol></li>
    <li>not exercise executive or board functions in relation to that business;</li>
    <li>not seek to influence a decision about that business;</li>
    <li>not make any written representations (whether by letter, facsimile or some other form of electronic communication) in relation to that business; and</li>
    <li>not make any oral representations (whether in person or some form of electronic communication) in respect of that business or immediately cease to make such oral representations when the prejudicial interest becomes apparent.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">(2) Where you have a prejudicial interest in any business of your authority you may attend a meeting but only for the purpose
  of making representations, answering questions or giving evidence relating to the business, provided that the public are also allowed to attend the meeting for the same purpose, whether under a statutory right or otherwise.</p>
<P style="margin-left:20px;text-indent:-20px;">(3) Sub-paragraph (1) does not prevent you attending and participating in a meeting if-</p>
<ol type="a">
  <li>you are required to attend a meeting of an overview or scrutiny committee, by such committee exercising its statutory powers; or</li>
<li>you have the benefit of a dispensation provided that you-
  <ol type="i">
    <li>state at the meeting that you are relying on the dispensation; and</li>
<li>before or immediately after the close of the meeting give written notification to your authority containing-
  <ol type="A">
    <li>details of the prejudicial interest;</li>
    <li>details of the business to which the prejudicial interest relates;</li>
    <li>details of, and the date on which, the dispensation was granted; and<</li>
    <li>your signature.</li>
  </ol></li>
  </ol>
</ol>

<p style="margin-left:20px;text-indent:-20px;">(4) Where you have a prejudicial interest and are making written or oral representations to your authority in reliance upon a dispensation, you must provide details of the dispensation within any such written or oral representations and, in the latter case, provide written notification to your authority within 14 days of making the representation.</p>

<h2>PART 4 THE REGISTER OF MEMBERS’ INTERESTS</h2>
<h3>Registration of Financial &amp; Other Interests &amp; Memberships &amp; Management Positions</h3>
<p style="margin-left:20px;text-indent:-20px;">15. (1) Subject to sub-paragraph (3), you must, within 28 days of-<ol type="a">
  <li>your authority’s code of conduct being adopted or the mandatory provisions of this model code being applied to your authority; or</li>
  <li>your election or appointment to office (if that is later), register your financial interests and other interests, where they fall within a category mentioned in Paragraph 10(2)(a) in your authority’s register maintained under Section 81(1) of the Local Government Act 2000 by providing written notification to your authority’s monitoring officer.</li>
</ol>

<p style="margin-left:20px;text-indent:-20px;">(2) You must, within 28 days of becoming aware of any new personal interest or change to any personal interest registered under sub-paragraph (1), register that new personal interest or change by providing written notification to your authority’s monitoring officer.</p>

<p style="margin-left:20px;text-indent:-20px;">(3) Sub-paragraphs (1) and (2) do not apply to sensitive information determined in accordance with paragraph 16(1).</p>

<p style="margin-left:20px;text-indent:-20px;">(4) Sub-paragraph (1) will not apply if you are a member of a relevant authority which is a community council when you act in your capacity as a member of such an authority.</p>

<h3>Sensitive Information</h3>
<p style="margin-left:20px;text-indent:-20px;">(1) Where you consider that the information relating to any of your personal interests is sensitive information, and your
  authority’s monitoring officer agrees, you need not include that information when registering that interest, or, as the case may be, a change to the interest under paragraph 15.</p>

<p style="margin-left:20px;text-indent:-20px;">(2) You must, within 28 days of becoming aware of any change of circumstances which means that information excluded under sub-paragraph (1) is no longer sensitive information, notify your authority’s monitoring officer asking that the information be included in your authority’s register of members’ interests.</p>

<p style="margin-left:20px;text-indent:-20px;">(3) In this code, “sensitive information” (“gwybodaeth sensitive”)means information whose availability for inspection by the
public creates, or is likely to create, a serious risk that you or a person who lives with you may be subjected to violence or intimidation.</p>

<h3>Registration of Gifts and Hospitality</h3>
<p style="margin-left:20px;text-indent:-20px;">17. You must, within 28 days of receiving any gift, hospitality, material benefit or advantage above a value specified in a resolution of your authority, provide written notification to your authority’s monitoring officer of the existence and nature of that gift, hospitality, material benefit or advantage.</p>
